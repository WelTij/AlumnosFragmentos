package com.example.tvwearos;

public class Alumnos {

    String matricula;
    String nombre;
    String curp;
    String apellidos;
    String sexo;
    String edad;
    String image_url;

    public Alumnos(String matricula, String nombre, String curp, String apellidos, String sexo, String edad, String image_url) {
        this.matricula = matricula;
        this.nombre = nombre;
        this.curp = curp;
        this.apellidos = apellidos;
        this.sexo = sexo;
        this.edad = edad;
        this.image_url = image_url;
    }
}

